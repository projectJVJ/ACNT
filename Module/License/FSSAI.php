                    <h3 class="text-center mt-2">FSSAI License Registration</h3>
                    <p>Any business involved in the production, handling, distribution, or sale of food in India is required to comply with the Food Safety and Standards Act by obtaining registration or a license from the Food Safety and Standards Authority of India (FSSAI). This ensures that all food products meet essential hygiene and safety standards.</p>
                    <p>Whether you're a small vendor or a large food manufacturer, the appropriate FSSAI registration is mandatory to operate legally and maintain consumer trust.</p>

                    <h4>Overview of FSSAI Registration and Licensing</h4>
                    <p>Under <strong>Section 31(1) of the Food Safety and Standards Act, 2006</strong>, all Food Business Operators (FBOs) must obtain an FSSAI license or registration, depending on their business scale and operations. <strong>FSSAI (Licensing and Registration of Food Businesses) Regulations, 2011</strong> outlines the eligibility and process for securing these licenses.</p>

                    <section>
                        <h4>Who Needs an FSSAI License?</h4>
                        <p>FSSAI licensing is mandatory for entities involved in:</p>
                        <ui>
                            <li>Food manufacturing, packaging, or processing</li>
                            <li>Food transportation or storage</li>
                            <li>Wholesale or retail food sales</li>
                            <li>Import/export of food items</li>
                            <li>Online food delivery or aggregator services</li>
                            <li>Restaurants, cafes, catering units, or cloud kitchens</li>
                        </ui>
                        <p>Both individuals and companies operating in the food sector are required to hold a valid FSSAI license before commencing operations.</p>
                        <h4>Categories of FSSAI Registration</h4>
                        <ol>
                            <li>FSSAI Basic Registration</li>
                            <p>Intended for petty food businesses and small-scale operators:</p>
                            <ul>
                                <li>Annual turnover does not exceed ₹12 lakhs</li>
                                <li>Daily food production up to 100 kg/liters (excluding milk/meat/fish)</li>
                                <li>Milk handling not exceeding 500 liters per day</li>
                                <li>Slaughtering capacity: up to 10 small animals, 2 large animals, or 50 poultry birds per day</li>
                                <li>Mobile vendors, hawkers, and small retailers</li>
                            </ul>
                            <p>The registration number consists of 14 digits, starting with “2” and is issued by the <strong>State Licensing Authority</strong>.</p>

                            <li>State FSSAI License</li>
                            <p>Applicable for medium-sized businesses with:</p>
                            <ul>
                                <li>Annual turnover above ₹12 lakhs but below ₹20 crores</li>
                                <li>Businesses involved in manufacturing, distribution, transportation, or processing</li>
                            </ul>
                            <p>The license is granted by the <strong>respective State Authority</strong> based on the nature and size of the business.</p>

                            <li>Central FSSAI License</li>
                            <p>Compulsory for:</p>
                            <ul>
                                <li>Large food businesses with turnover exceeding ₹20 crores</li>
                                <li>Units involved in import/export or supplying food to government institutions</li>
                                <li>Operators managing food supply chains across multiple states</li>
                            </ul>
                            <p>Issued by the <strong>Central Government</strong>, this license number starts with “1”.</p>
                        </ol>

