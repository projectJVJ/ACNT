                        <h4>Required Documents for FSSAI License</h4>
                        <p><strong>For Non-Manufacturing Businesses:</strong></p>
                        <ul>
                            <li>Identity and address proof of business owner/partners/directors</li>
                            <li>Proof of business premises (Rent Agreement/Sale Deed/Utility Bill)</li>
                            <li>List of partners/directors and nomination of authorized signatory</li>
                            <li>Constitution documents like Partnership Deed or MOA & AOA</li>
                            <li>Form IX – Nomination Form</li>
                            <li>Certificate under Co-operative Act (if applicable)</li>
                        </ul>

                        <p><strong>For Manufacturing/Processing Units:</strong></p>
                        <ul>
                            <li>Layout plan of the facility</li>
                            <li>List of machinery and installed capacity</li>
                            <li>Water test report (if water is used as an ingredient)</li>
                            <li>Photographs of production unit</li>
                            <li>Details of raw materials and source</li>
                            <li>IEC code (for importers/exporters)</li>
                        </ul>

                        <h4>Validity and Renewal</h4>
                        <p>FSSAI licenses are issued for a period ranging from 1 to 5 years, based on the applicant’s preference. Businesses must apply for renewal at least 30 days before the license expiry to avoid penalties or suspension.</p>

