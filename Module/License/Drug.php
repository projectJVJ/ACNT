                        <h4>Types of Drug Licenses</h4>
                        <ul>
                            <li><strong>Retail Drug License:</strong> Required for pharmacies and medical stores selling medicines directly to consumers.</li>
                            <li><strong>Wholesale Drug License:</strong> Issued to distributors who supply medicines in bulk to retailers or institutions.</li>
                            <li><strong>Manufacturing License:</strong> Mandatory for companies involved in producing drugs or cosmetics.</li>
                            <li><strong>Loan License:</strong> For businesses using another licensed facility to manufacture medicines.</li>
                            <li><strong>Import License:</strong> Needed for importing pharmaceutical products into India.</li>
                            <li><strong>Multi-Drug License:</strong> Required for businesses operating in multiple states or having multiple outlets.</li>
                        </ul>

                        <h4>Who Needs a Drug License?</h4>
                        <ul>
                            <li>Pharmacists and retail pharmacies</li>
                            <li>Wholesale drug distributors</li>
                            <li>Pharmaceutical manufacturers</li>
                            <li>Importers of drugs and cosmetics</li>
                            <li>Dealers in Ayurvedic, Siddha, or Unani medicines</li>
                        </ul>

                        <h4>Eligibility Criteria</h4>
                        <ul>
                            <li><strong>Qualified Personnel:</strong> Pharmacists (for retail) or experienced individuals (for wholesale)</li>
                            <li><strong>Premises:</strong> Adequate space, cleanliness, and required equipment (refrigerators, storage)</li>
                            <li><strong>No criminal background:</strong> Applicant must not have any drug-related convictions</li>
                            <li><strong>Registration:</strong> With State Pharmacy Council (for pharmacists)</li>
                        </ul>

                        <h4>Documents Required</h4>
                        <ul>
                            <li>Application Form (Form 19)</li>
                            <li>Identity and address proof of proprietor/partners</li>
                            <li>Educational qualification and registration certificate of pharmacist or competent person</li>
                            <li>Affidavits and photographs of the applicant and pharmacist</li>
                            <li>Blueprint/layout of premises</li>
                            <li>Electricity bill and refrigerator purchase bill</li>
                            <li>Ownership proof or rent agreement of premises</li>
                        </ul>

                        <h4>Validity and Renewal</h4>
                        <p>Drug Licenses are valid for 5 years and must be renewed before expiry. Timely renewal is essential to avoid penalties or cancellation.</p>

