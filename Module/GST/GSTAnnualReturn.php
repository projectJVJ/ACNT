                    <h3 class="text-center">GST Annual Return Filing - GSTR-9</h3>
                    <p>GSTR-9 is an annual return form that must be filed by regular taxpayers registered under the Goods and Services Tax (GST) regime. It provides a comprehensive summary of all outward and inward supplies made during the financial year, consolidating monthly or quarterly filings such as GSTR-1 and GSTR-3B. This includes transactions under CGST, SGST, and IGST.</p>
                    <p>This filing helps ensure transparency and provides the government with a complete view of a taxpayer’s annual financial activity under GST.</p>

                    <h4>Overview of GSTR-9</h4>
                    <p>GSTR-9 consolidates all monthly and quarterly GST returns filed during a financial year into one comprehensive report. It includes transaction summaries, tax liabilities, input tax credit claimed, and adjustments made. Businesses with an annual turnover above ₹2 crore are required to file this return, while it remains optional for those below the threshold.</p>

                    <h4>What is GSTR-9?</h4>
                    <p>GSTR-9 is a detailed summary of a registered taxpayer’s purchases and sales throughout the year. It includes tax liabilities, input tax credit (ITC) claimed, and reconciliations with the returns filed during the year. Filing GSTR-9 is mandatory for taxpayers with an annual turnover exceeding ₹2 crore, while it remains optional for those below this threshold.</p>

                    <h4>Who Should File GSTR-9?</h4>
                    <p>The GSTR-9 filing requirement applies to various categories:</p>
                    <section>
                        <ul>
                            <li><strong>Regular Taxpayers</strong>: Individuals and businesses registered under the regular GST scheme.</li>
                            <li><strong>SEZ Units/Developers</strong>: Entities operating within Special Economic Zones.</li>
                            <li><strong>Transitioned Taxpayers</strong>: Those who migrated from the Composition Scheme to regular registration during the year.</li>
                        </ul>

                        <h4>Who is Exempt from GSTR-9 Filing?</h4>
                        <ul>
                            <li>Taxpayers under the Composition Scheme (must file GSTR-9A instead).</li>
                            <li>Casual and Non-resident taxable persons.</li>
                            <li>Input Service Distributors (ISD).</li>
                            <li>OIDAR service providers.</li>
                        </ul>

                        <h4>Types of GST Annual Returns</h4>
                        <ul>
                            <li><strong>GSTR-9:</strong> Standard return for regular taxpayers.</li>
                            <li><strong>GSTR-9A:</strong> Applicable to taxpayers under the composition scheme.</li>
                            <li><strong>GSTR-9C:</strong> Reconciliation statement for taxpayers requiring an audit (turnover exceeding ₹5 crore).</li>
                        </ul>

                        <h4>GSTR-9C and GST Audit</h4>
                        <p>GSTR-9C is a reconciliation statement matching GSTR-9 data with audited financials. This form must be certified by a Chartered Accountant or Cost Accountant for businesses exceeding the audit threshold.</p>

                        <h4>Turnover Limit for GSTR-9</h4>
                        <p>Filing GSTR-9 is mandatory for businesses with annual turnover exceeding ₹2 crore. For businesses below this limit, the return is optional but recommended for transparency and compliance.</p>

                        <h4>GSTR-9 Due Date</h4>
                        <p>The deadline to file GSTR-9 is typically 31st December of the subsequent financial year. This date may be extended by government notifications.</p>

                        <h4>GSTR-9 Format Overview</h4>
                        <p>The return is divided into six parts:</p>
                        <ul>
                            <li><strong>Part 1:</strong> Basic taxpayer information.</li>
                            <li><strong>Part 2:</strong> Outward supplies and inward supplies on which tax is payable.</li>
                            <li><strong>Part 3:</strong> Input Tax Credit (ITC) details.</li>
                            <li><strong>Part 4:</strong> Tax paid during the year.</li>
                            <li><strong>Part 5:</strong> Transactions related to the previous financial year declared late.</li>
                            <li><strong>Part 6:</strong> Miscellaneous information including HSN summary, demands, refunds, and late fees.</li>
                        </ul>

                        <h4>Required Documents for GSTR-9 Filing</h4>
                        <ul>
                            <li>Monthly GST returns (GSTR-1, GSTR-3B, GSTR-2A).</li>
                            <li>Annual financial statements (P&L and balance sheet).</li>
                            <li>Reconciliation statement (GSTR-9C), if applicable.</li>
                        </ul>

                        <h4>Eligibility Criteria for GSTR-9C</h4>
                        <p>Taxpayers with turnover exceeding the audit threshold must file GSTR-9C, prepared and certified by a qualified professional.</p>

                        <h4>Steps to File GSTR-9</h4>
                        <ol>
                            <li>Login to the GST portal.</li>
                            <li>Select the relevant financial year under ‘Annual Returns.’</li>
                            <li>Choose whether to file a Nil return.</li>
                            <li>Fill in the required details across various parts of the form.</li>
                            <li>Save the draft and review the summary.</li>
                            <li>Compute any liabilities and pay applicable late fees, if any.</li>
                            <li>Submit using DSC or EVC authentication.</li>
                        </ol>

                        <h4>Late Fees for GSTR-9 Filing</h4>
                        <p>Late submission of GSTR-9 attracts a penalty of ₹200 per day (₹100 CGST + ₹100 SGST), capped at 0.25% of the taxpayer’s turnover in the respective State/UT.</p>

