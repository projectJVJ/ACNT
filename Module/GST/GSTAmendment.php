                    <h4>Step-by-Step Process for GST Registration Amendment</h4>
                    <ol>
                        <li>Login to the GST Portal at <a href="https://www.gst.gov.in" target="_blank">www.gst.gov.in</a>.</li>
                        <li>Navigate to Services &gt; Registration &gt; Amendment of Registration Non-Core/Core Fields.</li>
                        <li>Select the appropriate section that needs to be amended.</li>
                        <li>Update the details and upload any supporting documents, if required.</li>
                        <li>Submit the application using Digital Signature Certificate (DSC) or EVC.</li>
                        <li>Track the status using the ARN generated after submission.</li>
                    </ol>

                    <h4>Key Documents Required</h4>
                    <ul>
                        <li>Proof of change (e.g., utility bill, lease deed, board resolution)</li>
                        <li>Authorization letter for authorized signatory changes</li>
                        <li>Latest bank statement for bank detail updates</li>
                        <li>Photographs of promoters/partners (if applicable)</li>
                    </ul>

                    <h4>Timelines and Legal Provisions</h4>
                    <ul>
                        <li>Amendment application must be filed within 15 days of the change.</li>
                        <li>GST officer may approve or reject the amendment request within 15 working days.</li>
                        <li>If no response is received within the timeline, the application is deemed approved.</li>
                    </ul>

                    <h4>Consequences of Non-Amendment</h4>
                    <ul>
                        <li>Legal action in case of incorrect or outdated registration data</li>
                        <li>Mismatch in tax returns and invoices</li>
                        <li>Inability to update e-way bill data accurately</li>
                        <li>Penalties for non-disclosure of business changes</li>
                    </ul>

                    <h4>Additional Scenarios Requiring Amendment</h4>
                    <ul>
                        <li>Change in business constitution (e.g., proprietorship to partnership)</li>
                        <li>Merger or demerger of business units</li>
                        <li>Closure or transfer of a place of business</li>
                        <li>Appointment or removal of directors/partners</li>
                    </ul>

                    <h4>Frequently Asked Questions (FAQs)</h4>
                    <ul>
                        <li><strong>Can I change my PAN number through amendment?</strong><br>No, any change in PAN requires new GST registration.</li>
                        <li><strong>Is approval required for bank detail change?</strong><br>No, it is a non-core field and can be updated without approval.</li>
                        <li><strong>Can I track amendment status?</strong><br>Yes, using the ARN (Acknowledgement Reference Number) on the GST portal.</li>
                        <li><strong>What happens if I upload the wrong document?</strong><br>You may receive a notice from the GST Officer to resubmit the correct one.</li>
                        <li><strong>Can multiple amendments be made in one application?</strong><br>Yes, multiple fields can be amended in a single application.</li>
                    </ul>

