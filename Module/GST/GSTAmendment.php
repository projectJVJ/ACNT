                    <p>GST Registration Amendment refers to the process of updating or modifying the information submitted at the time of obtaining GST registration. Businesses are required to keep their registration details accurate and updated to avoid penalties and ensure compliance with the Goods and Services Tax (GST) Act.</p>

                    <h4>Types of Amendments in GST Registration</h4>
                    <p>Amendments in GST registration are broadly classified into two categories:</p>
                    <ul>
                        <li><strong>Core Field Amendments:</strong> These include changes in legal name, principal place of business, and additional places of business. Such amendments may require approval from the tax officer.</li>
                        <li><strong>Non-Core Field Amendments:</strong> These include changes in contact details, bank account, and authorized signatory. These can be updated online without approval from the tax officer.</li>
                    </ul>

