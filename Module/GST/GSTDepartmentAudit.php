                    <h4>Penalties for Non-Compliance Found in Audit</h4>
                    <p>Failing to comply with GST audit requirements or concealment of facts may lead to the following penalties:</p>
                    <ul>
                        <li><strong>Monetary Penalties</strong>: Up to 100% of the tax amount due in case of fraud.</li>
                        <li><strong>Interest</strong>: Applicable on delayed or underpaid taxes.</li>
                        <li><strong>Prosecution</strong>: In severe cases, legal action or arrest can be initiated.</li>
                    </ul>

