                    <h4>Key Components of a GST Department Audit</h4>
                    <ul>
                        <li><strong>Tax Return Verification</strong>: Comparison of filed GST returns with actual books of accounts to ensure accuracy.</li>
                        <li><strong>Invoice Scrutiny</strong>: Checking GST compliance of invoices issued and received.</li>
                        <li><strong>ITC Review</strong>: Verifying input tax credit claimed against eligible purchases and services.</li>
                        <li><strong>Exemption and Deduction Validation</strong>: Ensuring all claimed exemptions are genuine and supported by documentation.</li>
                        <li><strong>Stock and Inventory Check</strong>: Cross-verification of stock reported in returns with actual inventory records.</li>
                    </ul>

