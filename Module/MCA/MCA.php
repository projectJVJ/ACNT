                        <h3 class="mb-4 text-center">Registered Office Address Change: Process & Compliance</h3>
                        <p>A company's registered office is its legally recognized address where all statutory documents and formal notices are delivered. This location is registered with the Registrar of Companies (ROC) and is reflected in vital legal documents such as the Memorandum and Articles of Association.</p>

                        <h4 class="mt-4">Why Companies Change Their Registered Office</h4>
                        <section>
                        <ul>
                            <li><strong>Strategic Relocation:</strong> To improve accessibility, reduce costs, or expand operations.</li>
                            <li><strong>Business Expansion:</strong> Mergers or organizational growth may necessitate larger or more strategic spaces.</li>
                            <li><strong>Operational Efficiency:</strong> Proximity to markets, suppliers, or better infrastructure.</li>
                            <li><strong>Regulatory Compliance:</strong> Situational needs might demand relocating to meet jurisdictional requirements.</li>
                            <li><strong>Brand Positioning:</strong> Moving to a prominent area can enhance corporate image and credibility.</li>
                        </ul>

                        <h4 class="mt-4">Scenarios for Office Address Change</h4>
                        <p>Each type of address change involves specific legal steps. Below are the primary scenarios:</p>
                        <ul>
                            <li>Within the same city, town, or village</li>
                            <li>From one city to another in the same ROC jurisdiction</li>
                            <li>Between jurisdictions of two ROCs within the same state</li>
                            <li>From one state to another state</li>
                        </ul>

                        <h5 class="mt-4">Change Within Local Limits</h5>
                        <ul>
                            <li>Hold a Board Meeting and pass a resolution.</li>
                            <li>File Form INC-22 within 15 days to the ROC.</li>
                            <li>Submit address proof, utility bill, and ownership/rental documents.</li>
                            <li>Update official documents like letterheads and signboards.</li>
                        </ul>

                        <h5 class="mt-4">Change to Another City (Same ROC)</h5>
                        <ul>
                            <li>Board Resolution and Special Resolution in General Meeting required.</li>
                            <li>File MGT-14 with special resolution details.</li>
                            <li>File INC-22 with proof of the new address.</li>
                        </ul>

                        <h5 class="mt-4">Change Across ROC Jurisdictions (Same State)</h5>
                        <ul>
                            <li>Board and Special Resolutions are passed.</li>
                            <li>Consent obtained from creditors and stakeholders.</li>
                            <li>Application filed with the Regional Director using Form INC-23.</li>
                            <li>ROC filings done via MGT-14, INC-28, and INC-22.</li>
                        </ul>

                        <h5 class="mt-4">Change to Another State</h5>
                        <ul>
                            <li>Board and General Meetings to pass necessary resolutions.</li>
                            <li>Alteration of Memorandum of Association (MOA).</li>
                            <li>Notices published in newspapers and sent to stakeholders.</li>
                            <li>Regional Director's approval required via Form INC-23.</li>
                            <li>File MGT-14, INC-28, and INC-22 post-approval.</li>
                            <li>Notify all authorities and update records accordingly.</li>
                        </ul>

