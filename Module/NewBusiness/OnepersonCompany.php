                            <li>Separate legal entity and limited liability.</li>
                            <li>Easier fundraising opportunities.</li>
                            <li>Reduced compliance under Companies Act, 2013.</li>
                            <li>Simple incorporation and efficient management.</li>
                            <li>Perpetual succession ensures business continuity.</li>
                        </ul>
                    </section>

                    <section class="section-title">
                        <h4>Disadvantages of One Person Company</h4>
