                    <section class="section-title">
                        <h4>Eligibility Criteria to Register One Person Company</h4>
                        <ul>
                            <li>Must be a natural person and an Indian citizen.</li>
                            <li>Must have resided in India for at least 182 days in the previous calendar year.</li>
                            <li>Minimum authorized capital: ₹1,00,000.</li>
                            <li>Mandatory nominee appointment during incorporation.</li>
                            <li>Cannot engage in financial, banking, or insurance businesses.</li>
                            <li>Must convert to Pvt Ltd Co. if capital exceeds ₹50L or turnover exceeds ₹2Cr.</li>
                        </ul>
                    </section>

