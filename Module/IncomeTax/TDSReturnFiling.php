                    <h4>Understanding TDS</h2>
                        <p>TDS involves tax collection at the time of making specified payments such as salary,
                            interest, commission, rent, or professional fees. The payer (deductor) deducts a portion
                            of
                            the payment and remits it to the government on behalf of the payee (deductee).</p>

                        <h4>Who is Required to Deduct TDS?</h2>
                            <p>TDS must be deducted by individuals, firms, HUFs, companies, and other entities
                                meeting
                                specific turnover thresholds or falling under statutory requirements. TAN
                                registration is a
                                prerequisite.</p>

                            <h4>Due Dates for TDS Deposit</h2>
                                <p>TDS deducted must be deposited by the 7th of the following month. For TDS
                                    deducted in March,
                                    it should be deposited by April 30 in most cases.</p>

                                <h4>Common Transactions Subject to TDS</h2>
                                    <section>
                                        <ul>
                                            <li>Salary payments</li>
                                            <li>Interest on deposits (excluding securities)</li>
                                            <li>Contractor and sub-contractor fees</li>
                                            <li>Professional and technical service charges</li>
                                            <li>Rental payments</li>
                                        </ul>

                                        <h4>TDS Threshold Limits</h2>
                                            <ul>
                                                <li><strong>Rent:</strong> TDS if annual rent exceeds ₹2,40,000</li>
