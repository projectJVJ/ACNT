                                                    financial year</li>
                                                <li><strong>Professional Fees:</strong> Exceeds ₹30,000 annually
                                                </li>
                                                <li><strong>Interest (Non-Securities):</strong> ₹40,000 (non-senior
                                                    citizens), ₹50,000
                                                    (senior citizens)</li>
