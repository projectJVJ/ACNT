                        <section>
                            <h4>What is RCMC Certificate?</h4>
                            <p>The RCMC (Registration Cum Membership Certificate) is a crucial document for businesses
                                involved in the import and export of restricted goods. It certifies the registration of
                                an
                                exporter with the relevant Export Promotion Council (EPC), Commodity Board, or Export
                                Development Authority. RCMC is required to avail benefits under the Foreign Trade Policy
                                (FTP).</p>
                            <h4>Why RCMC Registration?</h4>
                            <ul>
                                <li>Authorization to import/export restricted products under FTP.</li>
                                <li>Evidence of registration with the relevant council or board.</li>
                                <li>Access to FTP concessions and schemes.</li>
                            </ul>
                            <h4>Eligibility</h4>
                            <ul>
                                <li>Exporter or merchant engaged in import/export business.</li>
                                <li>Must have an Import Export Code (IEC) issued by DGFT.</li>
                                <li>Declaration of primary business line and relevant board authority.</li>
                                <li>Approval from FIEO if no specific board exists for the product.</li>
                            </ul>
                            <h4>Benefits of RCMC</h4>
                            <ul>
                                <li>Access to export incentive schemes like MEIS, MAI.</li>
                                <li>Streamlined customs clearance.</li>
                                <li>Enhanced exporter credibility.</li>
                                <li>Up-to-date market information from EPCs and boards.</li>
                            </ul>
                            <h4>Issuing Authorities</h4>
                            <p>RCMC is issued by authorized bodies under DGFT including:</p>
                            <ul>
                                <li>Export Promotion Councils (e.g. APEDA, AEPC, EEPC, FIEO)</li>
                                <li>Commodity Boards (e.g. Tea Board, Coffee Board, Spices Board)</li>
                                <li>Development Authorities (e.g. ELCID, NCTI)</li>
                            </ul>
                            <p>Exporters not covered by any board can apply to FIEO.</p>
                            <h4>Validity</h4>
                            <p>RCMC is valid for 5 years from April 1 of the licensing year to March 31 of the fifth
                                year.
                            </p>
                            <h4>Required Documents</h4>
                            <ul>
                                <li>IEC issued by DGFT</li>
                                <li>PAN card</li>
                                <li>MOA or Partnership/Trust Deed</li>
                                <li>Registrar certificate (if address changed)</li>
                                <li>CA-certified foreign exchange earnings (3 years)</li>
                                <li>Board resolution or POA for signing authority</li>
                                <li>GST registration certificate</li>
                            </ul>
                            <h4>Types of Memberships</h4>
                            <ul>
                                <li>Ordinary Membership</li>
                                <li>Associate Membership</li>
                                <li>Premier Trading House (PTH)</li>
                                <li>Star Export House (SEH)</li>
                                <li>Trading House (TH)</li>
                            </ul>
                            <h4>Compliance Requirements</h4>
                            <ul>
                                <li>RCMC renewal every 5 years</li>
                                <li>Adherence to FTP guidelines</li>
                                <li>Annual export data submission</li>
                                <li>Compliance with customs and foreign trade regulations</li>
                                <li>Maintain export transaction records</li>
                            </ul>
